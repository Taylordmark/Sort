class RetinaNet(keras.Model):
    """A subclassed Keras model implementing the RetinaNet architecture.

    Attributes:
      num_classes: Number of classes in the dataset.
      backbone: The backbone to build the feature pyramid from.
        Currently supports ResNet50 only.
    """

    def __init__(self, num_classes, backbone=None, min_confidence=.05, max_iou=.5,**kwargs):
        super().__init__(name="RetinaNet", **kwargs)


        self.fpn = FeaturePyramid(backbone)
        self.num_classes = num_classes

        prior_probability = tf.constant_initializer(-np.log((1 - 0.01) / 0.01))
        self.cls_head = build_head(9 * num_classes, prior_probability)
        self.box_head = build_head(9 * 4, "zeros")
        self.min_confidence = min_confidence
        self.max_iou = max_iou
        #self.decoder =  OldDecodePredictions(confidence_threshold=self.min_confidence, nms_iou_threshold=float(self.max_iou), max_detections=100000)
        self.decoder = DecodePredictions(confidence_threshold=float(self.min_confidence), nms_iou_threshold=float(self.max_iou), max_detections=100000)

    def call(self, image, training=False):
        features = self.fpn(image, training=training)
        N = tf.shape(image)[0]
        cls_outputs = []
        box_outputs = []
        for feature in features:
            box_outputs.append(tf.reshape(self.box_head(feature), [N, -1, 4]))
            cls_outputs.append(
                tf.reshape(self.cls_head(feature), [N, -1, self.num_classes])
            )


        cls_outputs = tf.concat(cls_outputs, axis=1)
        box_outputs = tf.concat(box_outputs, axis=1)
        predictions = tf.concat([box_outputs, cls_outputs], axis=-1)

        return predictions
        #return DecodePredictions(confidence_threshold=float(self.min_confidence), nms_iou_threshold=float(self.max_iou))(image, predictions)
    
    def predict(self, image):
        predictions = self.call(image)

        return self.decoder(image, predictions)
    

